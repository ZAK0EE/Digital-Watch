#ifndef DateAndTime_H_
#define DateAndTime_H_

/********************************************************************************************************/
/************************************************Includes************************************************/
/********************************************************************************************************/

#include "../../lib/StdTypes.h"

/********************************************************************************************************/
/************************************************Defines*************************************************/
/********************************************************************************************************/



/********************************************************************************************************/
/************************************************Types***************************************************/
/********************************************************************************************************/

// Structure definition for time
typedef struct
{
    // H1H2 : M1M2 : S1S2
    u8 H1;
    u8 H2;
    u8 M1;
    u8 M2;
    u8 S1;
    u8 S2;
} Time_t;

// Structure definition for date
typedef struct
{
    // D1D2 : M1M2 : Y1Y2Y3Y4
    u8 D1;
    u8 D2;
    u8 M1;
    u8 M2;
    u8 Y1;
    u8 Y2;
    u8 Y3;
    u8 Y4;
} Date_t;

// Enum for digits of date and time
typedef enum {
    H1,
    H2,
    M1,
    M2,
    S1,
    S2,
    D1,
    D2,
    Mo1,
    Mo2,
    Y1,
    Y2,
    Y3,
    Y4,
} Digits_t;

/********************************************************************************************************/
/************************************************APIs****************************************************/
/********************************************************************************************************/

// API function to edit date and time
void edit_DateTime(u8 frame);

// Getter API for time
Time_t getTime(void);

// Getter API for date
Date_t getDate(void);

// Getter API for current digit being edited
Digits_t getCurrentDigit(void);


#endif // DateAndTime_H_